package com.ayon.androidfirebase;

public class Info {
    private String name;
    private String age;
    private String desc;

    public Info() {
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getDesc() {
        return desc;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }


}
